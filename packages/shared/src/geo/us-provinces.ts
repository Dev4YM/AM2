import type { GeoProvince } from "./types";

function p(
  code: string,
  name: string,
  regionId: string,
  center: { lat: number; lng: number },
  bounds: { north: number; south: number; east: number; west: number },
): GeoProvince {
  return {
    id: `US-${code}`,
    code,
    name,
    regionId,
    countryCode: "US",
    center,
    bounds: {
      north: bounds.north,
      south: bounds.south,
      east: bounds.east,
      west: bounds.west,
    },
  };
}

